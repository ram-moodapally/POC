    private void btnOrderToMfrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderToMfrActionPerformed
        // TODO add your handling code here:
        
        
        MfrWorkRequest manuRequest = new MfrWorkRequest();

        manuRequest.setMfrMedicine(txtMedicineName.getText());
        manuRequest.setMfrQuantity(Integer.parseInt(txtQuantity.getText()));
        manuRequest.setStatus("Ordered");
        System.out.println("manu button"+manuRequest.getStatus());

        Organization org = null;
        for (Network network : system.getNetworkList()) {
            

            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {

                if (enterprise instanceof ManufacturingEnterprise) 
                {
                    //System.out.println("entreprise"+enterprise.getName());

                    for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                        if (organization instanceof ManufacturingOrganization) {
                            
                            org = organization;
                            //System.out.println("inside phars work request "+org.getName());
                            break;
                        }
                    }
                }
            }
        }
        //System.out.println("outside phars work request "+org.getName());
        if (org != null) {
            org.getWorkQueue().getWorkRequestList().add(manuRequest);
            userAccount.getWorkQueue().getWorkRequestList().add(manuRequest);

            JOptionPane.showMessageDialog(this, "Order Placed to Manufacturer!");
        }
    }//GEN-LAST:event_btnOrderToMfrActionPerformed


